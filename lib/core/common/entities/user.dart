class User {
  final String id;
  final String email;
  final String name;
  final String ageGroup;
  final String mobile;
  final String gender;
  final String nationality;

  User({
    required this.id,
    required this.email,
    required this.name,
    required this.ageGroup,
    required this.mobile,
    required this.gender,
    required this.nationality,
  });
}
